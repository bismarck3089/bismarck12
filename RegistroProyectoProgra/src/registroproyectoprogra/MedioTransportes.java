package registroproyectoprogra;

public class MedioTransportes {

    private double velocidad;
    private boolean equipado;
    private double costoAdquision;
    private int añosVidaUtil;

    public double getVelocidad() {
        return velocidad;
    }

    public void setVelocidad(double velocidad) {
        this.velocidad = velocidad;
    }

    public boolean isEquipado() {
        return equipado;
    }

    public void setEquipado(boolean equipado) {
        this.equipado = equipado;
    }

    public double getCostoAdquision() {
        return costoAdquision;
    }

    public void setCostoAdquision(double costoAdquision) {
        this.costoAdquision = costoAdquision;
    }

    public int getAñosVidaUtil() {
        return añosVidaUtil;
    }

    public void setAñosVidaUtil(int añosVidaUtil) {
        this.añosVidaUtil = añosVidaUtil;
    }

}
